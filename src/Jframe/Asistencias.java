    private void mostrarAsistencias() {
        try (Connection con = ConexionBD.conectar();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery("SELECT id_asistencia, id_empleado, hora_entrada, hora_salida, fecha, estado, observaciones FROM asistencias")) {

            DefaultTableModel modelo = new DefaultTableModel(
                new Object[]{"ID Asistencia", "ID Empleado", "Hora Entrada", "Hora Salida", "Fecha", "Estado", "Observación"}, 0
            );

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("id_asistencia"),
                    rs.getInt("id_empleado"),
                    rs.getString("hora_entrada"),
                    rs.getString("hora_salida"),
                    rs.getString("fecha"),
                    rs.getString("estado"),
                    rs.getString("observaciones")
                });
            }

            tablaAsistencias.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al mostrar asistencias: " + e.getMessage());
        }
    }

    private void limpiarCampos() {
        txtIdAsistencia.setText("");
        txtIdEmpleado.setText("");
        txtHoraEntrada.setText("");
        txtHoraSalida.setText("");
        txtFecha.setText("");
        txtEstado.setText("");
        txtObservacion.setText("");
    }

    private void limpiarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tablaAsistencias.getModel();
        modelo.setRowCount(0);
    }

