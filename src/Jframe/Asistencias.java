    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if (txtIdEmpleado.getText().isEmpty() || txtHoraEntrada.getText().isEmpty() ||
            txtHoraSalida.getText().isEmpty() || txtFecha.getText().isEmpty() || txtEstado.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos obligatorios.");
            return;
        }

        try (Connection con = ConexionBD.conectar()) {
            String sql = "INSERT INTO asistencias (id_empleado, hora_entrada, hora_salida, fecha, estado, observaciones) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(txtIdEmpleado.getText()));
            ps.setString(2, txtHoraEntrada.getText());
            ps.setString(3, txtHoraSalida.getText());
            ps.setString(4, txtFecha.getText());
            ps.setString(5, txtEstado.getText());
            ps.setString(6, txtObservacion.getText());
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Asistencia registrada correctamente.");
            mostrarAsistencias();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
         if (txtIdAsistencia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecciona una asistencia para editar.");
            return;
        }

        try (Connection con = ConexionBD.conectar()) {
            String sql = "UPDATE asistencias SET id_empleado=?, hora_entrada=?, hora_salida=?, fecha=?, estado=?, observaciones=? WHERE id_asistencia=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(txtIdEmpleado.getText()));
            ps.setString(2, txtHoraEntrada.getText());
            ps.setString(3, txtHoraSalida.getText());
            ps.setString(4, txtFecha.getText());
            ps.setString(5, txtEstado.getText());
            ps.setString(6, txtObservacion.getText());
            ps.setInt(7, Integer.parseInt(txtIdAsistencia.getText()));
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Asistencia actualizada correctamente.");
            mostrarAsistencias();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tablaAsistencias.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una asistencia para eliminar.");
            return;
        }

        int id = Integer.parseInt(tablaAsistencias.getValueAt(fila, 0).toString());

        try (Connection con = ConexionBD.conectar()) {
            String sql = "DELETE FROM asistencias WHERE id_asistencia=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, id);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Asistencia eliminada correctamente.");
            mostrarAsistencias();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

