    private void mostrarDepartamentos() {
        try (Connection con = ConexionBD.conectar();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery("SELECT id_departamento, nombre_departamento, ubicacion FROM departamentos")) {

            DefaultTableModel modelo = new DefaultTableModel(
                new Object[]{"ID Departamento", "Nombre", "Ubicación"}, 0
            );

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("id_departamento"),
                    rs.getString("nombre_departamento"),
                    rs.getString("ubicacion")
                });
            }

            tablaDepartamentos.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al mostrar: " + e.getMessage());
        }
    }

    private void limpiarCampos() {
        txtId.setText("");
        txtNombre.setText("");
        txtUbicacion.setText("");
    }

    private void limpiarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tablaDepartamentos.getModel();
        modelo.setRowCount(0);
    }
