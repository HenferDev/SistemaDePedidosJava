    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if (txtNombre.getText().isEmpty() || txtUbicacion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos antes de agregar.");
            return;
        }

        try (Connection con = ConexionBD.conectar()) {
            String sql = "INSERT INTO departamentos (nombre_departamento, ubicacion) VALUES (?, ?)";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, txtNombre.getText());
            ps.setString(2, txtUbicacion.getText());
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Departamento agregado correctamente.");
            mostrarDepartamentos();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar: " + e.getMessage());
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
     int fila = tablaDepartamentos.getSelectedRow();
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona un departamento para editar.");
        return;
    }

    try (Connection con = ConexionBD.conectar()) {
        String sql = "UPDATE departamentos SET nombre_departamento=?, ubicacion=? WHERE id_departamento=?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, txtNombre.getText());
        ps.setString(2, txtUbicacion.getText());
        ps.setInt(3, Integer.parseInt(txtId.getText()));
        ps.executeUpdate();

        JOptionPane.showMessageDialog(this, "Departamento actualizado correctamente.");
        mostrarDepartamentos();
        limpiarCampos();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al actualizar: " + e.getMessage());
    }
        
    }//GEN-LAST:event_btnEditarActionPerformed

