    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
            if (txtIdCliente.getText().isEmpty() || txtFecha.getText().isEmpty() ||
            txtHora.getText().isEmpty() || txtMetodoPago.getText().isEmpty() ||
            txtEstado.getText().isEmpty() || txtTotal.getText().isEmpty() ||
            txtIdDepartamento.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Completa todos los campos antes de agregar.");
            return;
        }

        try (Connection con = ConexionBD.conectar()) {
            String sql = "INSERT INTO pedidos (id_cliente, fecha, hora, metodo_pago, estado, total_pagar, id_departamento) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(txtIdCliente.getText()));
            ps.setString(2, txtFecha.getText());
            ps.setString(3, txtHora.getText());
            ps.setString(4, txtMetodoPago.getText());
            ps.setString(5, txtEstado.getText());
            ps.setDouble(6, Double.parseDouble(txtTotal.getText()));
            ps.setInt(7, Integer.parseInt(txtIdDepartamento.getText()));
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Pedido agregado correctamente.");
            mostrarPedidos();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar pedido: " + e.getMessage());
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int fila = tablaPedidos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un pedido para editar.");
            return;
        }

        try (Connection con = ConexionBD.conectar()) {
            String sql = "UPDATE pedidos SET id_cliente=?, fecha=?, hora=?, metodo_pago=?, estado=?, total_pagar=?, id_departamento=? WHERE id_pedido=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(txtIdCliente.getText()));
            ps.setString(2, txtFecha.getText());
            ps.setString(3, txtHora.getText());
            ps.setString(4, txtMetodoPago.getText());
            ps.setString(5, txtEstado.getText());
            ps.setDouble(6, Double.parseDouble(txtTotal.getText()));
            ps.setInt(7, Integer.parseInt(txtIdDepartamento.getText()));
            ps.setInt(8, Integer.parseInt(txtId.getText()));
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Pedido actualizado correctamente.");
            mostrarPedidos();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar pedido: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tablaPedidos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un pedido para eliminar.");
            return;
        }

        int id = Integer.parseInt(tablaPedidos.getValueAt(fila, 0).toString());

        try (Connection con = ConexionBD.conectar()) {
            String sql = "DELETE FROM pedidos WHERE id_pedido=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, id);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Pedido eliminado correctamente.");
            mostrarPedidos();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar pedido: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

