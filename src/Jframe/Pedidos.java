    private void mostrarPedidos(){
    try (Connection con = ConexionBD.conectar();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery("SELECT id_pedido, id_cliente, fecha, hora, metodo_pago, estado, total_pagar, id_departamento FROM pedidos")) {

            DefaultTableModel modelo = new DefaultTableModel(
                new Object[]{"ID Pedido", "ID Cliente", "Fecha", "Hora", "Método de Pago", "Estado", "Total a Pagar", "ID Depto"}, 0
            );

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("id_pedido"),
                    rs.getInt("id_cliente"),
                    rs.getString("fecha"),
                    rs.getString("hora"),
                    rs.getString("metodo_pago"),
                    rs.getString("estado"),
                    rs.getDouble("total_pagar"),
                    rs.getInt("id_departamento")
                });
            }

            tablaPedidos.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al mostrar pedidos: " + e.getMessage());
        }
    }

