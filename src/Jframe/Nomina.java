  private void mostrarNominas() {
    try (Connection con = ConexionBD.conectar();
         Statement st = con.createStatement();
         ResultSet rs = st.executeQuery(
             "SELECT id_nomina, id_empleado, id_departamento, mes, anio, salario_base, horas_extras, bonificacion, descuentos, total_pagar, fecha_pago FROM nominas"
         )) {

        DefaultTableModel modelo = new DefaultTableModel(
            new Object[]{"ID Nómina", "ID Empleado", "ID Departamento", "Mes", "Año", 
                         "Salario Base", "Horas Extras", "Bonificación", "Descuentos", 
                         "Total a Pagar", "Fecha de Pago"}, 0
        );

        while (rs.next()) {
            modelo.addRow(new Object[]{
                rs.getInt("id_nomina"),
                rs.getInt("id_empleado"),
                rs.getInt("id_departamento"),
                rs.getString("mes"),
                rs.getString("anio"),
                rs.getDouble("salario_base"),
                rs.getDouble("horas_extras"),
                rs.getDouble("bonificacion"),
                rs.getDouble("descuentos"),
                rs.getDouble("total_pagar"),
                rs.getString("fecha_pago")
            });
        }

        tablaNominas.setModel(modelo);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al mostrar nóminas: " + e.getMessage());
    }
}

