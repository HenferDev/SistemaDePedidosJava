    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
         if (txtIdEmpleado.getText().isEmpty() || txtMes.getText().isEmpty() || 
            txtAnio.getText().isEmpty() || txtSalarioBase.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos obligatorios.");
            return;
        }

        try (Connection con = ConexionBD.conectar()) {
            double salario = Double.parseDouble(txtSalarioBase.getText());
            double extras = Double.parseDouble(txtHorasExtras.getText());
            double bonif = Double.parseDouble(txtBonificacion.getText());
            double desc = Double.parseDouble(txtDescuentos.getText());
            double total = salario + extras + bonif - desc;
            txtTotalPagar.setText(String.valueOf(total));

            String fechaPago = txtFechaPago.getText().isEmpty() ? LocalDate.now().toString() : txtFechaPago.getText();

            String sql = "INSERT INTO nominas (id_empleado, id_departamento, mes, anio, salario_base, horas_extras, bonificacion, descuentos, total_pagar, pagada, fecha_pago) "
           + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, 1, ?)";

            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(txtIdEmpleado.getText()));
            ps.setInt(2, Integer.parseInt(txtIdDepartamento.getText())); 
            ps.setString(3, txtMes.getText());
            ps.setString(4, txtAnio.getText());
            ps.setDouble(5, salario);
            ps.setDouble(6, extras);
            ps.setDouble(7, bonif);
            ps.setDouble(8, desc);
            ps.setDouble(9, total);
            ps.setString(10, fechaPago);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Nómina agregada correctamente.");
            mostrarNominas();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar nómina: " + e.getMessage());
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int fila = tablaNominas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una nómina para editar.");
            return;
        }

        int id = Integer.parseInt(tablaNominas.getValueAt(fila, 0).toString());

        try (Connection con = ConexionBD.conectar()) {
            double salario = Double.parseDouble(txtSalarioBase.getText());
            double extras = Double.parseDouble(txtHorasExtras.getText());
            double bonif = Double.parseDouble(txtBonificacion.getText());
            double desc = Double.parseDouble(txtDescuentos.getText());
            double total = salario + extras + bonif - desc;
            txtTotalPagar.setText(String.valueOf(total));

            String sql = "UPDATE nominas SET id_empleado=?, id_departamento=?, mes=?, anio=?, salario_base=?, horas_extras=?, bonificacion=?, descuentos=?, total_pagar=?, fecha_pago=? WHERE id_nomina=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(txtIdEmpleado.getText()));
            ps.setInt(2, Integer.parseInt(txtIdDepartamento.getText()));
            ps.setString(3, txtMes.getText());
            ps.setString(4, txtAnio.getText());
            ps.setDouble(5, salario);
            ps.setDouble(6, extras);
            ps.setDouble(7, bonif);
            ps.setDouble(8, desc);
            ps.setDouble(9, total);
            ps.setString(10, txtFechaPago.getText());
            ps.setInt(11, id);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Nómina actualizada correctamente.");
            mostrarNominas();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al editar nómina: " + e.getMessage());
        }
        
        
    }//GEN-LAST:event_btnEditarActionPerformed

