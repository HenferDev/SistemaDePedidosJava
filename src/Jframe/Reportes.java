    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
         int fila = tablaReportes.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un reporte para editar.");
            return;
        }

        try (Connection con = ConexionBD.conectar()) {
            String sql = "UPDATE reportes SET tipo_reporte=?, descripcion=?, generado_por=?, total_registros=?, observaciones=? WHERE id_reporte=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, txtTipoReporte.getText());
            ps.setString(2, txtDescripcion.getText());
            ps.setString(3, txtGeneradoPor.getText());
            ps.setInt(4, Integer.parseInt(txtTotalRegistros.getText()));
            ps.setString(5, txtObservaciones.getText());
            ps.setInt(6, Integer.parseInt(txtIdReporte.getText()));
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Reporte actualizado correctamente.");
            mostrarReportes();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al editar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
         int fila = tablaReportes.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un reporte para eliminar.");
            return;
        }

        int id = Integer.parseInt(tablaReportes.getValueAt(fila, 0).toString());
        try (Connection con = ConexionBD.conectar()) {
            String sql = "DELETE FROM reportes WHERE id_reporte=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, id);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Reporte eliminado correctamente.");
            mostrarReportes();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

