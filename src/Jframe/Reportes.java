    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if (txtTipoReporte.getText().isEmpty() || txtDescripcion.getText().isEmpty()
                || txtGeneradoPor.getText().isEmpty() || txtTotalRegistros.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos antes de agregar.");
            return;
        }

        try (Connection con = ConexionBD.conectar()) {
            String sql = "INSERT INTO reportes (tipo_reporte, fecha_generacion, hora_generacion, descripcion, generado_por, total_registros, observaciones) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = con.prepareStatement(sql);

           
            LocalDate fechaActual = LocalDate.now();
            LocalTime horaActual = LocalTime.now();

            ps.setString(1, txtTipoReporte.getText());
            ps.setString(2, fechaActual.toString());
            ps.setString(3, horaActual.withSecond(0).withNano(0).toString());
            ps.setString(4, txtDescripcion.getText());
            ps.setString(5, txtGeneradoPor.getText());
            ps.setInt(6, Integer.parseInt(txtTotalRegistros.getText()));
            ps.setString(7, txtObservaciones.getText());

            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Reporte agregado correctamente.");
            mostrarReportes();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar reporte: " + e.getMessage());
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

