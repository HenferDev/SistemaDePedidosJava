    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String categoria = txtCategoria.getText();
        String precio = txtPrecio.getText();
        String stock = txtStock.getText();

        if (nombre.isEmpty() || categoria.isEmpty() || precio.isEmpty() || stock.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos.");
            return;
        }

        try (Connection con = ConexionBD.conectar()) {
            String sql = "INSERT INTO productos(nombre, categoria, precio, stock) VALUES (?, ?, ?, ?)";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, nombre);
            ps.setString(2, categoria);
            ps.setDouble(3, Double.parseDouble(precio));
            ps.setInt(4, Integer.parseInt(stock));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Producto agregado correctamente.");
            limpiarCampos();
            mostrarProductos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

